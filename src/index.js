const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    morseStr = expr
        .match(/.{1,10}/g)
        .map(el=> el.match(/.{1,2}/g))
        .map(el => el
            .filter(val => val != '00')
            .map(val => val == '10' ? '.': val == '11' ? '-' : '')
            )
        .map(el=> el.join(''))
        .map(el => MORSE_TABLE[el] || ' ')
        .join('')
        .trim()

        return morseStr
}

module.exports = {
    decode
}